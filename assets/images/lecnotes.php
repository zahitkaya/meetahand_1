     <h2>&emsp;&emsp; What is a model?</h2>
     <br>
     <p>&emsp;&emsp; A model is a partial representation of a system’s (dynamic) behavior. There is no the model for a system. 
         Many different models can be associated with the same system depending on what &emsp;level of approximation we desire. 
         The latter is a function of the purpose for the desired model. A model should be represented with a quality tag indicating 
         its fidelity in reproducing the system’s &emsp;behavior, or the range of signals it is valid for, or the size of approximation error we 
         may expect.</p>
